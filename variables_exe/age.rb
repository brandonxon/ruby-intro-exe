#asks a user how old, and tells how old they will be in 10, 20, 30, 40

puts "How old are you? please enter a number"

age = gets.chomp.to_i

puts "In 10 years you will be #{age + 10} \n"
puts "In 20 years you will be #{age + 20} \n"
puts "In 30 years you will be #{age + 30} \n"
puts "In 40 years you will be #{age + 40} \n"

